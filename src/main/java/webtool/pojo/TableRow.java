package webtool.pojo;

public class TableRow {
	String id;
	String name;
	String position;
	String salary;
	String start_date;
	String office;
	String ext;
	
	
	public TableRow(String id, String name, String position, String salary, String start_date, String office,
			String ext) {
		super();
		this.id = id;
		this.name = name;
		this.position = position;
		this.salary = salary;
		this.start_date = start_date;
		this.office = office;
		this.ext = ext;
	}


	public TableRow() {
		super();
	}
	
	
	
	
}
